Cypress.Commands.add('mountServices', () => {
  Cypress.env()
  cy.intercept('GET', `${Cypress.env('API_URL')}/registrations`).as('getRegistrations')
  cy.intercept('PUT', `${Cypress.env('API_URL')}/registrations/*`).as('editStatus')
  cy.intercept('POST', `${Cypress.env('API_URL')}/registrations`).as('addRegister')
  cy.intercept('DELETE', `${Cypress.env('API_URL')}/registrations/*`).as('deleteRegister')
});